Origine

Le genre Érable (Acer) est composé d’arbres de la famille des Sapindacées très répandus dans les zones tempérées fraîches et humides, du sud du Canada et des États-Unis en passant par l’Europe jusqu’à la côte pacifique de l’Asie. L’étymologie est complexe puisqu’en France, l’érable vient de l’ancien français acerabulus, composé d’acer, qui signifie aiguille en latin, et bulus qui viendrait du gaulois abalo pour pomme.

Les érables mesurent généralement entre 10 et 45 m de hauteur. Leurs feuilles sont nervurées et arborent leurs couleurs d’automne au mois d’octobre, de l’orange au violet, et leur feuillage est dense. Les fleurs d’érable, également très colorées (rouge, orange, jaune, vert), comportent 4 à 5 pétales très courts, voire inexistants. Après sa floraison, l’érable produit des fruits, qui sont appelés samare. Toujours groupés par deux, ils forment des hélices, ce qui permet à la graine qu’elle contient d’être facilement disséminée par le vent.
