Pinus est un genre d’arbres appartenant à la famille des Pinaceae et contenant une dizaine d’espèces. Les pins ont des feuilles en aiguilles regroupées par 2 ou 3 ou 5 et forment des fruits qui sont des cônes formés d’écailles à l’aisselle, où l’on retrouve les graines. C’est le plus vaste genre de conifères et beaucoup de ses espèces sont des essences d’arbres forestiers importantes.

Le bois des pins est surtout utilisé pour fabriquer du papier, de l’ameublement comme des parquets, des meubles, etc. Leurs bourgeons en revanche permettent de fabriquer des sirops ainsi que des bonbons. Enfin, les pignons de pin, comestibles, sont issus des graines du pin parasol. Il faut également savoir qu’il y a certaines espèces de pins, comme le pin sylvestre par exemple, qui sont utilisés en aromathérapie en raison de leurs nombreux bienfaits.

Le nom Pin vient du latin « pinus » et de l’indo-européen « pic », qui signifie amer.
Couleur, fleur, feuille

Pinus recouvre des espèces assez hétérogènes, bien que de nombreux caractères soient communs au genre entier : avec des tailles assez variées, le port est très souvent droit, et tous les résineux sont monoïques. Aussi, beaucoup des pins ont un tronc unique avec une écorce assez épaisse et très fissurée. En ce qui concerne les espèces ayant un port érigé, la cime est moins pointue et est plus étalée, et les branches basses tombent en mourant et découvrent un tronc élancé.

Les rameaux des pins ont pour particularité d’être dimorphes, avec des branches fortes et longues, alors que d’autres seront grêles et courtes, de couleur verte ou grise, et poilues ou non. Les rameaux sont très petits mesurent seulement quelques centimètres et ne possèdent qu’un seul bouquet d’aiguilles en leur extrémité.

Les branches présentent des feuilles avortées en forme d’écaille et poussent de manière horizontale, en longueur, et par leurs bourgeons terminaux. Ces bourgeons sont variables en taille, couleur et en forme, dépendamment de l’espèce, et sont résineux ou non. Ils sont plus clairs que les aiguilles : jaunes, dressés et décoratifs.

